<li>The mock test can be given 3 times by a student. The student has to book the slot, give exam and view the answer keys and challenge for the mock test.</li>
<li>Your slot details will appear on the dashboard once your payment is confirmed for the subject(s). You can book your exam time from this page. The slot time once booked can be changed later with payment of a nominal fee. </li>
<li>The students have to appear for the exam as per the time slot selected by them.</li>
<li>In Mock Tests, the ranking will not be declared.</li>
<li>The answer keys for Basic and Advanced level will be available only on the above-mentioned dates. During this period, the students can challenge the questions too.</li>
<li>Basic and Advanced Level exam results will be declared within a month.</li>
<li>The above schedule may change under exceptional circumstances.</li>
